

public class Main
{
    //main method, starting point for all java apps.
    //when your java application starts, main method starts first. If you want your project runnable, this is vital.
    public static void main(String[] args)
    {
        //a print statement lets us print things directly to the console.
        //It's the most common built in form of sending out information to the console.
        int age = 20;
        System.out.println("Hello World!");

        System.out.print("This prints out a line without adding a new line marker (aka without starting a new line)");

        System.out.printf("This line prints out a formatted string. Useful for displaying variables within scripts. Such as your age: %d", age);

        //
    }
}